/**
 * omniroute setup podman — Print and (optionally) execute the right
 * Podman invocation for the user's scenario. Podman is a drop-in
 * replacement for Docker on most distros and the default rootless
 * container runtime on RHEL/Fedora/Windows-WSL2 setups.
 *
 * Three runtime differences vs. Docker that we handle here:
 *   1. `docker compose` → `podman compose` (plugin on v4+, or
 *      `podman-compose` PyPI tool). We detect by spawning `podman compose
 *      version` and falling back to `podman-compose`.
 *   2. `docker build` → `podman build` (syntax-compatible).
 *   3. `docker run` → `podman run` plus `--userns=keep-id` hint when
 *      mounting host directories for the `host` profile.
 *
 * Quadlet integration: on systemd-hosts (Linux), Podman Quadlet files
 * (`*.container`, `*.volume`) go in `~/.config/containers/systemd/` and
 * boot the container as a systemd unit. We emit a minimal `.container`
 * unit alongside the `quadlet` action.
 *
 * Conventions match `setup-electron` / `setup-docker` (--json, --run,
 * pure inner runner, ../io.mjs for prints).
 */

import { spawnSync } from "node:child_process";
import { mkdirSync, writeFileSync, existsSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import os from "node:os";

import { printHeading, printInfo, printSuccess, printError } from "../io.mjs";
import { t } from "../i18n.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const REPO_ROOT = resolve(__dirname, "..", "..", "..");

export const PODMAN_ACTIONS = ["build", "run", "compose", "quadlet"];

/** Detect host platform. Exposed for tests. */
export function detectPlatform() {
  return process.platform;
}

/**
 * Detect whether `podman compose` (built-in) or `podman-compose` (PyPI)
 * is available. Returns one of: "podman-compose", "podman", "missing".
 *
 * @returns {"podman-compose" | "podman" | "missing"}
 */
export function detectComposeBackend() {
  const probe = spawnSync("podman", ["compose", "version"], {
    encoding: "utf8",
    shell: false,
  });
  if (probe.status === 0) return "podman-compose";
  const probe2 = spawnSync("podman-compose", ["--version"], {
    encoding: "utf8",
    shell: false,
  });
  if (probe2.status === 0) return "podman-compose";
  return "missing";
}

/** Default action per platform — `compose` is universal. */
export function defaultActionForPlatform(_p) {
  return "compose";
}

/**
 * Render the right `podman` command for the chosen action + profile.
 *
 * @param {string} action "build" | "run" | "compose" | "quadlet"
 * @param {string} [profile]
 * @param {string} [platform=process.platform]
 * @param {"podman-compose"|"podman"|"missing"} [composeBackend]
 * @returns {string}
 */
export function renderCommand(
  action,
  profile,
  platform = process.platform,
  composeBackend = "podman-compose"
) {
  if (action === "build") {
    return `podman build --target runner-base -t omniroute:base .`;
  }
  if (action === "run") {
    const parts = [
      "podman run --rm -it",
      "  --name omniroute",
      "  -p 20128:20128",
      "  -p 20129:20129",
      "  -v omniroute-data:/app/data",
    ];
    // When mounting host dirs (the `host` profile) we need uid mapping
    // for rootless podman.
    if (profile === "host") {
      parts.push("  --userns=keep-id");
    }
    parts.push("  omniroute:base");
    return parts.join(" ");
  }
  if (action === "compose") {
    const flag = profile ? `--profile ${profile}` : "--profile base";
    if (composeBackend === "podman-compose") {
      return `podman compose ${flag} up -d --build`;
    }
    // Fallback to podman-compose (PyPI).
    return `podman-compose ${profile ? `--profile ${profile}` : ""} up -d --build`.replace(
      /\s+/g,
      " "
    );
  }
  // action === "quadlet"
  if (platform === "win32") {
    // Quadlets are a Linux-only systemd feature. WSL2 users get the hint.
    return "echo 'Quadlet units require a systemd host (Linux/WSL2). Generate the .container file and copy it into WSL.'";
  }
  // Linux: tell the user where to drop the file.
  return "install ./omniroute.container ~/.config/containers/systemd/ && systemctl --user daemon-reload && systemctl --user start omniroute.service";
}

/**
 * Generate a minimal Podman Quadlet `.container` unit for OmniRoute.
 *
 * @param {string} [profile="base"]
 * @param {number} [port=20128]
 * @returns {string}
 */
export function buildQuadletUnit(profile = "base", port = 20128) {
  // Quadlet syntax is INI-ish; lines starting with `#` are comments.
  return [
    "# Generated by: omniroute setup podman quadlet --profile " + profile,
    "# Drop into ~/.config/containers/systemd/ then:",
    "#   systemctl --user daemon-reload",
    "#   systemctl --user start omniroute.service",
    "",
    "[Unit]",
    "Description=OmniRoute (profile: " + profile + ")",
    "After=network-online.target",
    "",
    "[Container]",
    "Image=omniroute:" + profile,
    "PublishPort=" + port + ":" + port,
    "Volume=omniroute-data:/app/data",
    "AutoUpdate=registry",
    "",
    "[Service]",
    "Restart=always",
    "",
    "[Install]",
    "WantedBy=default.target",
    "",
  ].join("\n");
}

/**
 * Inner action runner. Pure (no Commander coupling).
 *
 * @param {{ action?: string, profile?: string, run?: boolean, json?: boolean, output?: string, targetDir?: string }} opts
 * @returns {Promise<{ exitCode: number, payload?: object }>}
 */
export async function runSetupPodmanCommand(opts = {}) {
  const platform = detectPlatform();
  const action = opts.action || defaultActionForPlatform(platform);
  const wantsJson = Boolean(opts.json || opts.output === "json");
  const wantsRun = Boolean(opts.run);

  if (!PODMAN_ACTIONS.includes(action)) {
    const msg = `Unknown podman action '${action}'. Valid: ${PODMAN_ACTIONS.join(", ")}`;
    if (wantsJson) return { exitCode: 2, payload: { error: msg, valid: PODMAN_ACTIONS } };
    printError(msg);
    return { exitCode: 2 };
  }

  const profile = opts.profile || "base";
  const composeBackend = detectComposeBackend();

  // Quadlet output dir defaults to ~/.config/containers/systemd/ on Linux.
  const targetDir =
    opts.targetDir ||
    (platform === "win32"
      ? join(process.cwd(), "podman-quadlet")
      : join(os.homedir(), ".config", "containers", "systemd"));

  const cmd = renderCommand(action, profile, platform, composeBackend);
  const quadletUnit = action === "quadlet" ? buildQuadletUnit(profile) : null;

  const payload = {
    platform,
    action,
    profile,
    composeBackend,
    command: cmd,
    quadletUnit,
    quadletTargetDir: action === "quadlet" ? targetDir : null,
    executed: false,
    wroteQuadletFile: false,
  };

  if (wantsJson) {
    console.log(JSON.stringify(payload, null, 2));
  } else {
    printHeading("OmniRoute → Podman setup");
    printInfo(`Action: ${action}${profile ? `  Profile: ${profile}` : ""}`);
    printInfo(`Compose backend: ${composeBackend}`);
    printInfo(`Command: ${cmd}`);
    if (action === "quadlet") {
      printInfo(`Quadlet unit will be written to: ${targetDir}/omniroute.container`);
    }
    if (composeBackend === "missing" && action === "compose") {
      printError("No `podman compose` / `podman-compose` binary found on PATH.");
      printInfo("Install podman-compose (pip install podman-compose) or upgrade podman ≥ 4.1.");
    }
  }

  // Write the Quadlet file when the action is quadlet and the user asked for it.
  if (action === "quadlet") {
    if (platform === "win32" && !opts.targetDir) {
      // Native Windows has no systemd; we just print the unit.
      if (!wantsJson) {
        console.log("");
        console.log("── omniroute.container (preview) ──");
        console.log(quadletUnit);
      }
    } else {
      try {
        mkdirSync(targetDir, { recursive: true });
        const outPath = join(targetDir, "omniroute.container");
        if (wantsRun) {
          writeFileSync(outPath, quadletUnit, "utf8");
          payload.wroteQuadletFile = true;
          if (!wantsJson) printSuccess(`Wrote ${outPath}`);
        } else if (!wantsJson) {
          console.log("");
          console.log("── omniroute.container (preview) ──");
          console.log(quadletUnit);
          printInfo(`(pass --run to actually write this file to ${outPath})`);
        }
      } catch (err) {
        if (!wantsJson) printError(`Failed to write quadlet: ${err.message}`);
        return { exitCode: 3, payload: wantsJson ? payload : undefined };
      }
    }
  }

  if (!wantsRun) {
    if (!wantsJson && action !== "quadlet") {
      console.log("");
      printInfo(`Next: ${cmd}`);
      printInfo(`(pass --run to execute it now)`);
    }
    return { exitCode: 0, payload: wantsJson ? payload : undefined };
  }

  // --run for non-quadlet actions: shell out.
  if (composeBackend === "missing" && action === "compose") {
    if (!wantsJson) printError("Refusing to execute: no podman compose backend found.");
    return { exitCode: 4, payload: wantsJson ? payload : undefined };
  }

  if (!wantsJson) printInfo("Executing…");
  const argv = cmd.split(/\s+/).filter(Boolean);
  const bin = argv[0];
  const args = argv.slice(1);
  const res = spawnSync(bin, args, {
    cwd: REPO_ROOT,
    stdio: "inherit",
    shell: false,
  });
  payload.executed = true;
  const exitCode = typeof res.status === "number" ? res.status : 1;
  if (!wantsJson && exitCode === 0) printSuccess("Podman command complete.");
  return { exitCode, payload: wantsJson ? payload : undefined };
}

/**
 * Register the `omniroute setup podman` subcommand on the parent setup
 * command.
 *
 * @param {import("commander").Command} setupCommand
 */
export function registerSetupPodman(setupCommand) {
  setupCommand
    .command("podman [action]")
    .description(
      t("setup.podman") ||
        "Print the right podman command (build | run | compose | quadlet) for rootless/container-on-systemd hosts"
    )
    .option(
      "--profile <name>",
      "Compose profile (see docker-compose.yml: base, web, cli, host, cliproxyapi, memory, bifrost)"
    )
    .option(
      "--target-dir <dir>",
      "Override the Quadlet target directory (default: ~/.config/containers/systemd/)"
    )
    .option(
      "--run",
      "Execute the command / write the Quadlet unit instead of just printing it",
      false
    )
    .option("--json", "Emit a structured JSON payload and exit", false)
    .addHelpText(
      "after",
      `\nActions:\n` +
        PODMAN_ACTIONS.map((a) => `  ${a.padEnd(8)} → podman invocation`).join("\n") +
        `\n\nNotes:\n` +
        `  • On Linux with systemd, prefer 'quadlet' for boot-time containers.\n` +
        `  • WSL2 users: 'quadlet' works inside the WSL distro, not on the Windows host.\n` +
        `\nExamples:\n` +
        `  omniroute setup podman compose --profile base\n` +
        `  omniroute setup podman run --profile host --run\n` +
        `  omniroute setup podman quadlet --profile web --run\n` +
        `  omniroute setup podman --json\n`
    )
    .action(async (action, opts, cmd) => {
      const globalOpts = cmd.parent?.parent?.optsWithGlobals?.() ?? {};
      const merged = {
        ...opts,
        action: action || opts.action,
        output: globalOpts.output,
        json: Boolean(opts.json || globalOpts.output === "json"),
      };
      const { exitCode } = await runSetupPodmanCommand(merged);
      if (exitCode !== 0) process.exit(exitCode);
    });
}
